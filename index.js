function makeInt(string)
{
    return parseInt(string, 10);
}

function preserveDecimal(string)
{
    return parseFloat(string);
}

function add(a, b){
    return a + b
}

function subtract(b, a){
    return b - a
}

function multiply(a, b){
    return a * b
}

function divide(b, a){
    return b / a
}

function increment(a){
    return ++a
}

function decrement(a){
    return --a
}


